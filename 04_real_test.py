"""# 8. 計算 FID（每個模型 vs 整批生成圖）

FID 需要兩個資料夾：「參考真實圖」和「生成圖」
 因為是 real-world sketch，沒有 ground truth，
 所以用 clean-fid 的 "stats from folder" 模式：
 以 baseline 生成圖為參考分佈，比較各 LoRA 的生成分佈
（相對比較，不是絕對值）
"""

REFERENCE_DIR = f"{OUTPUT_DIR}/baseline"  # 以 baseline 為參考

fid_scores = {}
