"""# 6. 定義 Metric 函式"""

# --- CLIP Score ---
clip_model, _, clip_preprocess = open_clip.create_model_and_transforms(
    'ViT-L-14', pretrained='openai'
)
clip_model = clip_model.to("cuda").eval()
clip_tokenizer = open_clip.get_tokenizer('ViT-L-14')

def compute_clip_score(image: Image.Image, prompt: str) -> float:
    img_t = clip_preprocess(image).unsqueeze(0).to("cuda")
    txt_t = clip_tokenizer([prompt]).to("cuda")
    with torch.no_grad():
        i = clip_model.encode_image(img_t)
        i /= i.norm(dim=-1, keepdim=True)
        t = clip_model.encode_text(txt_t)
        t /= t.norm(dim=-1, keepdim=True)
    return (i * t).sum().item()

# --- Edge IoU ---
def compute_edge_iou(generated: Image.Image, sketch: Image.Image) -> float:
    gen_np  = np.array(generated.convert("RGB").resize((512, 512)))
    sk_np   = np.array(sketch.convert("RGB").resize((512, 512)))

    gen_gray  = cv2.cvtColor(gen_np, cv2.COLOR_RGB2GRAY)
    gen_edges = cv2.Canny(gen_gray, 50, 150)

    sk_gray = cv2.cvtColor(sk_np, cv2.COLOR_RGB2GRAY)
    _, sk_edges = cv2.threshold(sk_gray, 128, 255, cv2.THRESH_BINARY_INV)

    intersection = np.logical_and(gen_edges > 0, sk_edges > 0).sum()
    union        = np.logical_or(gen_edges > 0, sk_edges > 0).sum()
    return float(intersection / (union + 1e-6))

