"""# 7. 批次推論 + 計算指標"""

generator = torch.Generator("cuda").manual_seed(SEED)
records   = []

for sketch_path in tqdm(sketch_paths, desc="Sketches"):
    fname  = os.path.basename(sketch_path)
    sketch = Image.open(sketch_path).convert("RGB").resize((512, 512))

    row = {"filename": fname}

    for model_name, pipe in PIPELINES.items():
        gen = pipe(
            prompt           = PROMPT,
            negative_prompt  = NEG_PROMPT,
            image            = sketch,
            num_inference_steps = NUM_STEPS,
            guidance_scale   = GUIDANCE,
            generator        = generator,
        ).images[0]

        # 存生成圖
        save_path = f"{OUTPUT_DIR}/{model_name}/{fname}"
        gen.save(save_path)

        # 計算指標
        clip  = compute_clip_score(gen, PROMPT)
        eiou  = compute_edge_iou(gen, sketch)

        row[f"{model_name}_clip"]     = round(clip, 4)
        row[f"{model_name}_edge_iou"] = round(eiou, 4)

    records.append(row)

df = pd.DataFrame(records)
