results = []

for i in TEST_INDICES:
    sketch   = Image.open(f"{SKETCH_DIR}/{i:04d}.png").convert("RGB")
    gt_photo = Image.open(f"{PHOTO_DIR}/{i:04d}.png").convert("RGB")
    gen_kwargs = dict(
        prompt            = PROMPT,
        negative_prompt   = NEG_PROMPT,
        image             = sketch,
        num_inference_steps = 30,
        guidance_scale    = 7.5,
        generator         = torch.Generator(device).manual_seed(SEED)
    )

    result_baseline = pipe_baseline(**gen_kwargs).images[0]
    result_lora     = pipe_lora(**gen_kwargs).images[0]

    # ---------- 計算六指標 ----------
    row = {"idx": i}

    # 1. CLIP Score (ViT-L/14)
    row["clip_base"] = compute_clip_score(result_baseline, PROMPT)
    row["clip_lora"] = compute_clip_score(result_lora,     PROMPT)

    # 2. LPIPS（vs ground truth，越低越好）
    row["lpips_base"] = compute_lpips(result_baseline, gt_photo)
    row["lpips_lora"] = compute_lpips(result_lora,     gt_photo)

    # 3. SSIM（vs ground truth，越高越好）
    row["ssim_base"] = compute_ssim(result_baseline, gt_photo)
    row["ssim_lora"] = compute_ssim(result_lora,     gt_photo)

    # 4. DINOv2 Similarity（vs ground truth，越高越好）
    row["dino_base"] = compute_dino_similarity(result_baseline, gt_photo)
    row["dino_lora"] = compute_dino_similarity(result_lora,     gt_photo)

    # 5. ArcFace Identity Score（vs ground truth，越高越好）
    row["arcface_base"] = compute_arcface_score(result_baseline, gt_photo)
    row["arcface_lora"] = compute_arcface_score(result_lora,     gt_photo)

    # 6. Edge IoU（vs sketch，越高越好）
    row["edge_base"] = compute_edge_iou(result_baseline, sketch)
    row["edge_lora"] = compute_edge_iou(result_lora,     sketch)

    results.append(row)
    print(f"[{i:04d}] CLIP: {row['clip_base']:.4f}/{row['clip_lora']:.4f} | "
          f"LPIPS: {row['lpips_base']:.4f}/{row['lpips_lora']:.4f} | "
          f"DINOv2: {row['dino_base']:.4f}/{row['dino_lora']:.4f} | "
          f"ArcFace: {row['arcface_base']}/{row['arcface_lora']} | "
          f"Edge: {row['edge_base']:.4f}/{row['edge_lora']:.4f}")

df = pd.DataFrame(results)
