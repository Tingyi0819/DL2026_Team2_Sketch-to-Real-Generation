"""# 4. 定義六個 metric 函式

定義六個 metric 函式。每個函式上面有注釋說明衡量什麼、來源論文是哪篇
"""

import open_clip
import lpips
from transformers import AutoModel, AutoProcessor

# ------------------------------------------------------------------
# Metric 1：CLIP Score（ViT-L/14）
# ------------------------------------------------------------------
clip_model, _, clip_preprocess = open_clip.create_model_and_transforms(
    'ViT-L-14', pretrained='openai'
)
clip_model = clip_model.to(device).eval()
clip_tokenizer = open_clip.get_tokenizer('ViT-L-14')

def compute_clip_score(image: Image.Image, prompt: str) -> float:
    """
    語意一致性：生成圖 embedding 與 text embedding 的 cosine similarity。
    來源：Radford et al., 2021, "Learning Transferable Visual Models From Natural Language Supervision" (CLIP, ICML 2021)
    """
    img_t = clip_preprocess(image).unsqueeze(0).to(device)
    txt_t = clip_tokenizer([prompt]).to(device)
    with torch.no_grad():
        img_feat = clip_model.encode_image(img_t)
        txt_feat = clip_model.encode_text(txt_t)
        img_feat /= img_feat.norm(dim=-1, keepdim=True)
        txt_feat /= txt_feat.norm(dim=-1, keepdim=True)
    return (img_feat * txt_feat).sum().item()


# ------------------------------------------------------------------
# Metric 2：LPIPS（感知相似度，越低越好）
# ------------------------------------------------------------------
lpips_fn = lpips.LPIPS(net='vgg').to(device)

def compute_lpips(generated: Image.Image, ground_truth: Image.Image) -> float:
    """
    感知相似度：用 VGG 中間層 feature 計算，比 pixel-wise L2 更接近人眼。
    值域 0~1，越低越好。
    來源：Zhang et al., 2018, "The Unreasonable Effectiveness of Deep Features as a Perceptual Metric" (CVPR 2018)
    """
    def to_tensor(img):
        t = torch.from_numpy(np.array(img.resize((512, 512))).astype(np.float32) / 127.5 - 1.0)
        return t.permute(2, 0, 1).unsqueeze(0).to(device)
    with torch.no_grad():
        score = lpips_fn(to_tensor(generated), to_tensor(ground_truth))
    return score.item()


# ------------------------------------------------------------------
# Metric 3：SSIM（結構相似度，越高越好）
# ------------------------------------------------------------------
from skimage.metrics import structural_similarity as skimage_ssim

def compute_ssim(generated: Image.Image, ground_truth: Image.Image) -> float:
    """
    結構相似度：比較亮度、對比、結構三個維度，值域 0~1，越高越好。
    常與 LPIPS 搭配使用互補（SSIM 對模糊敏感，LPIPS 對感知細節敏感）。
    來源：Wang et al., 2004, "Image Quality Assessment: From Error Visibility to Structural Similarity" (IEEE TIP)
    """
    gen_arr = np.array(generated.resize((512, 512)))
    gt_arr  = np.array(ground_truth.resize((512, 512)))
    return skimage_ssim(gen_arr, gt_arr, channel_axis=2, data_range=255)


# ------------------------------------------------------------------
# Metric 4：DINOv2 Cosine Similarity（越高越好）
# ------------------------------------------------------------------
dino_processor = AutoProcessor.from_pretrained("facebook/dinov2-base")
dino_model = AutoModel.from_pretrained("facebook/dinov2-base").to(device).eval()

def compute_dino_similarity(generated: Image.Image, ground_truth: Image.Image) -> float:
    """
    視覺語意相似度：用 DINOv2 self-supervised ViT 提取 [CLS] token embedding，
    計算 cosine similarity。不受 text-image pairing bias 影響，對真實感判斷比 CLIP 更準。
    值域 -1~1，越高越好。
    來源：Oquab et al., 2023, "DINOv2: Learning Robust Visual Features without Supervision" (TMLR 2024)
    """
    def get_embedding(img):
        inputs = dino_processor(images=img, return_tensors="pt").to(device)
        with torch.no_grad():
            outputs = dino_model(**inputs)
        return outputs.last_hidden_state[:, 0, :]  # [CLS] token

    feat_gen = get_embedding(generated)
    feat_gt  = get_embedding(ground_truth)
    feat_gen = feat_gen / feat_gen.norm(dim=-1, keepdim=True)
    feat_gt  = feat_gt  / feat_gt.norm(dim=-1, keepdim=True)
    return (feat_gen * feat_gt).sum().item()


# ------------------------------------------------------------------
# Metric 5：ArcFace Identity Score（人臉 identity 相似度，越高越好）
# ------------------------------------------------------------------
import insightface
from insightface.app import FaceAnalysis

arcface_app = FaceAnalysis(name="buffalo_l", providers=["CUDAExecutionProvider", "CPUExecutionProvider"])
arcface_app.prepare(ctx_id=0, det_size=(512, 512))

def compute_arcface_score(generated: Image.Image, ground_truth: Image.Image) -> float:
    """
    人臉 identity 相似度：用 ArcFace (buffalo_l) 提取 512-dim face embedding，
    計算 cosine similarity。專門量「臉長得多像」，是人臉生成任務的核心指標。
    值域 -1~1，越高越好（>0.3 通常視為同一人）。
    來源：Deng et al., 2019, "ArcFace: Additive Angular Margin Loss for Deep Face Recognition" (CVPR 2019)
    注意：若偵測不到人臉（如 sketch 太抽象），回傳 None。
    """
    def get_face_embedding(img: Image.Image):
        img_bgr = cv2.cvtColor(np.array(img.resize((512, 512))), cv2.COLOR_RGB2BGR)
        faces = arcface_app.get(img_bgr)
        if len(faces) == 0:
            return None
        return faces[0].normed_embedding  # 已 L2-normalize

    emb_gen = get_face_embedding(generated)
    emb_gt  = get_face_embedding(ground_truth)

    if emb_gen is None or emb_gt is None:
        return None   # 偵測失敗，跳過這張
    return float(np.dot(emb_gen, emb_gt))


# ------------------------------------------------------------------
# Metric 6：Edge IoU（結構控制忠實度，越高越好）
# ------------------------------------------------------------------
def compute_edge_iou(generated: Image.Image, sketch: Image.Image) -> float:
    """
    邊緣 IoU：對生成圖跑 Canny，與輸入 sketch 的邊緣做 IoU，
    衡量「生成結果有沒有忠實跟著草圖的輪廓」。
    值域 0~1，越高越好。
    """
    gen_gray  = cv2.cvtColor(np.array(generated.resize((512, 512))), cv2.COLOR_RGB2GRAY)
    gen_edges = cv2.Canny(gen_gray, 50, 150)

    sk_gray = cv2.cvtColor(np.array(sketch.resize((512, 512))), cv2.COLOR_RGB2GRAY)
    _, sk_edges = cv2.threshold(sk_gray, 128, 255, cv2.THRESH_BINARY_INV)

    intersection = np.logical_and(gen_edges > 0, sk_edges > 0).sum()
    union        = np.logical_or(gen_edges > 0, sk_edges > 0).sum()
    return float(intersection / (union + 1e-6))


